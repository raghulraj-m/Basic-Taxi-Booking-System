
public class Booking {

    int taxi_id;
    char pickup;
    char drop;
    int pickup_time;

    public Booking(int taxi_id, char pickup, char drop, int pickup_time) {
        this.taxi_id = taxi_id;
        this.pickup = pickup;
        this.drop = drop;
        this.pickup_time = pickup_time;
    }
}
