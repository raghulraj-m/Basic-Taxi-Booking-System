public class Passenger {
    static int id = 1;
    String name;
    int age;
    String prefenece;
    int passenger_id;

    Passenger(String name, int age, String prefenece) {
        this.passenger_id = id;
        this.name = name;
        this.age = age;
        this.prefenece = prefenece;
    }
}
